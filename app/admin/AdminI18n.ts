export const adminCopy: Record<AppLanguage, AdminCopy> = {
    it: itAdminCopy,
    en: enAdminCopy,
    uk: ukAdminCopy,
    ru: ruAdminCopy,
    es: esAdminCopy,
    fr: frAdminCopy,
    de: deAdminCopy,
};

export const adminLocales: Record<AppLanguage, string> = {
    it: "it-IT",
    en: "en-US",
    uk: "uk-UA",
    ru: "ru-RU",
    es: "es-ES",
    fr: "fr-FR",
    de: "de-DE",
};

export function getAdminI18n(
    appLanguage?: string | null
) {
    const language =
        normalizeAppLanguage(appLanguage);

    return {
        language,
        locale:
            adminLocales[language] ?? adminLocales.en,
        t: adminCopy[language] ?? adminCopy.en,
    };
}

export function formatAdminDate(
    value: Date | string,
    language: AppLanguage
) {
    return new Intl.DateTimeFormat(
        adminLocales[language] ?? adminLocales.en,
        {
            day: "2-digit",
            month: "long",
            year: "numeric",
        }
    ).format(new Date(value));
}

export function formatAdminDateTime(
    value: Date | string,
    language: AppLanguage
) {
    return new Intl.DateTimeFormat(
        adminLocales[language] ?? adminLocales.en,
        {
            day: "2-digit",
            month: "short",
            year: "numeric",
            hour: "2-digit",
            minute: "2-digit",
        }
    ).format(new Date(value));
}

export function formatAdminMoney(
    value: number,
    currency: string,
    language: AppLanguage
) {
    return new Intl.NumberFormat(
        adminLocales[language] ?? adminLocales.en,
        {
            style: "currency",
            currency: currency || "USD",
            maximumFractionDigits: 2,
        }
    ).format(value);
}

export function valueLabel(
    t: AdminCopy,
    category: ValueCategory,
    value?: string | null
) {
    if (!value) {
        return "-";
    }

    const labels =
        t.valueLabels[category] as Record<string, string>;

    return labels[value] ?? value;
}
