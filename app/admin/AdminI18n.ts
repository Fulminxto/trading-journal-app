export type AdminCopy = typeof enAdminCopy;

type ValueCategory = keyof AdminCopy["valueLabels"];

function mergeValueLabels(
    base: AdminCopy["valueLabels"],
    override?: Partial<AdminCopy["valueLabels"]>
): AdminCopy["valueLabels"] {
    return {
        ticketType: {
            ...base.ticketType,
            ...override?.ticketType,
        },
        ticketStatus: {
            ...base.ticketStatus,
            ...override?.ticketStatus,
        },
        priority: {
            ...base.priority,
            ...override?.priority,
        },
        updateType: {
            ...base.updateType,
            ...override?.updateType,
        },
        accountStatus: {
            ...base.accountStatus,
            ...override?.accountStatus,
        },
    };
}

type AdminCopyOverride =
    Partial<Omit<AdminCopy, "valueLabels">> & {
        valueLabels?: Partial<AdminCopy["valueLabels"]>;
    };

function makeCopy(
    base: AdminCopy,
    override: AdminCopyOverride
): AdminCopy {
    return {
        ...base,
        ...override,
        valueLabels: mergeValueLabels(
            base.valueLabels,
            override.valueLabels
        ),
    };
}

