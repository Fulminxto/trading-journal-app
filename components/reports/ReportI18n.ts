export function getReportLabels(language?: string | null) {
    const appLanguage = normalizeAppLanguage(language);
    return labels[appLanguage] ?? labels.en;
}

export function formatReportCurrency(
    value: number,
    currency?: string | null,
    language?: string | null,
    maximumFractionDigits = 0
) {
    return formatCurrencyByLanguage(
        value,
        currency || "USD",
        language
    ).replace(/([,.]\d{2})$/, maximumFractionDigits === 0 ? "" : "$1");
}

export function formatReportDate(
    date: Date | string,
    language?: string | null
) {
    return formatDateByLanguage(date, language, {
        day: "2-digit",
        month: "long",
        year: "numeric",
    });
}
